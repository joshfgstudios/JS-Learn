//Numbers in JS have double precision 64 bit binary format (15-17 decimal places)

var val1 = 5.5;
var val2 = 5;

var sum = val1 + val2;
console.log("Addition: " + sum);

var letsDivide = 15 / 5;
console.log("Division: " + letsDivide);

var wannaMultiply = 6 * 3;
console.log("Multiplication: " + wannaMultiply);

var subtractMe = 12 - 4;
console.log("Subtraction: " + subtractMe);

var remainder = 16 % 5;
console.log("Remainder: " + remainder);

var val3 = 2;
var incremenet = val3++;
console.log("Increment Postfix: " + incremenet);

var val4 = 2;
var increment = ++val4;
console.log("Increment Prefix: " + increment);